import pytest

import directed


@pytest.mark.parametrize('first,second,expected', [(2, 3, False), (1, 1, True)])
def test_vertex_equal(first, second, expected):
    v1 = directed.Vertex(first)
    v2 = directed.Vertex(second)

    assert (v1 == v2) is expected


@pytest.mark.parametrize(
    'first,second,third,expected', [(1, 2, 3, False), (1, 2, 2, True)]
)
def test_edge_equal(first, second, third, expected):
    v1 = directed.Vertex(first)
    v2 = directed.Vertex(second)
    v3 = directed.Vertex(third)

    assert (directed.Edge(v1, v2) == directed.Edge(v1, v3)) is expected


@pytest.mark.parametrize(
    'first,second,third,expected', [(1, 2, 3, False), (1, 2, 2, True)]
)
def test_edges_equal(first, second, third, expected):
    graph_1 = directed.Graph()
    graph_2 = directed.Graph()
    graph_1.edges.add(directed.Edge(directed.Vertex(first), directed.Vertex(second)))
    graph_2.edges.add(directed.Edge(directed.Vertex(first), directed.Vertex(third)))

    assert (graph_1 == graph_2) is expected


@pytest.mark.parametrize('expected', [True, False])
def test_edge_in_edges(expected):
    edge = directed.Edge(directed.Vertex(1), directed.Vertex(2))
    graph = directed.Graph()
    if expected:
        graph.edges.add(edge)

    assert (edge in graph) is expected


@pytest.mark.parametrize('expected', [False, True])
def test_is_hamiltonian_cycle(expected):
    graph = directed.Graph()
    graph.edges.add(directed.Edge(directed.Vertex(1), directed.Vertex(2)))
    graph.edges.add(directed.Edge(directed.Vertex(2), directed.Vertex(3)))
    if expected:
        graph.edges.add(directed.Edge(directed.Vertex(3), directed.Vertex(1)))
    else:
        graph.edges.add(directed.Edge(directed.Vertex(3), directed.Vertex(5)))

    assert graph.is_hamiltonian_cycle() is expected


@pytest.mark.parametrize(
    'graph,expected',
    [
        ([(1, 2), (2, 1)], True),
        ([(1, 2), (2, 3)], False),
        ([(1, 2), (2, 3), (3, 4), (10, 11), (11, 10)], True),
        ([(1, 2), (2, 3), (3, 4), (4, 1)], True),
        (
            [
                (1, 2),
                (13, 18),
                (10, 22),
                (21, 17),
                (4, 3),
                (9, 13),
                (16, 19),
                (22, 11),
                (5, 14),
                (14, 6),
                (7, 12),
                (3, 20),
                (19, 9),
                (17, 23),
                (23, 10),
                (2, 15),
                (20, 16),
                (11, 1),
                (12, 21),
            ],
            False,
        ),
        ([(1, 2), (2, 3), (2, 4), (3, 5), (4, 6), (6, 7), (7, 8), (8, 1)], False),
    ],
)
def test_cycle_checker(graph: list, expected):
    graph_for_check = directed.Graph()
    for left, right in graph:
        graph_for_check.edges.add(
            directed.Edge(directed.Vertex(left), directed.Vertex(right))
        )

    assert graph_for_check.has_cycle() is expected


